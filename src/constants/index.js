    deskPosition: isMobile ? [0.5, 0.7, 0] : [0.7, 0.01, 2.9],
    cubePosition: isSmall ? [4, -5, 0] : isMobile ? [8, -8, 0] : isTablet ? [10, -9, 0] : [15, -10, 0],
